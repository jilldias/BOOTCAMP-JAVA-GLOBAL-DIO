public class DiferençaIdade {
    public static void main(String[] args) {
        int idadePessoa1 = 25;
        int idadePessoa2 = 40;
        int diferencaIdade = Math.abs(idadePessoa1 - idadePessoa2); // Calcula a diferença absoluta entre as idades
        System.out.printf("A diferença de idade entre as duas pessoas é: %d anos.\n", diferencaIdade);
    }
}
